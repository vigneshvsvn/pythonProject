name='Vigneshkumar'

print(name.isalnum())    ## [a-z,A-Z,0-9]
print(name.isalpha())    ## [a-z,A-Z]
print(name.islower())    ## all char are lower or not
print(name.isupper())    ##  to checck all char are in upper case or not
print(name.isdigit())    ## Check if all char are digit
print(name.isspace())    ## Return only it contains spaces




